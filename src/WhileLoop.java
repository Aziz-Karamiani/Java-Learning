public class WhileLoop {
    public static void main(String[] args) {
        // While Loop
        int i = 1;
        while (i <= 100) {
            System.out.println(i);
            i++;
        }

        System.out.println("-----------------");

        // do while
        i = 1;
        do {
            System.out.println(i);
            i++;
        }while (i <= 100);
    }
}
